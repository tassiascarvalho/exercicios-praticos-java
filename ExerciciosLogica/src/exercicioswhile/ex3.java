    public static void main(String[] args) {
        // 3. Faça um programa que exiba os números de 1 até 2000.
        Integer x = 1;// Inicialização
        // Variavel Contadora, controladora

        while (x <= 2000) {// Condição
            System.out.println(x); // Corpo
            x++; // iteração
        }
    }

